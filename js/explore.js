/*
- Function that takes a string as a parameter and reverses the string.
- Takes string as a parameter(ex:Apple) and reverses the string(Apple)
*/


function reverse(word){
	var string = '';
	for(var i = word.length - 1; i>=0; i--){
		string += word[i];
	}
	return string;
}

